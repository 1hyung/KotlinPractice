fun main(args: Array<String>) {
    // 이 표현식은 계산되지만 결과를 활용하지 않기 때문에 출력되지 않음
    4 + 29 - 50

    // 표현식의 결과를 출력
    println(4 + 29 - 50) // -17이 출력됨

    // 표현식의 결과를 변수에 저장
    val result = 4 + 29 - 50

    // 변수에 저장된 결과를 출력
    println(result) // -17이 출력됨

    // 큰따옴표로 감싼 텍스트는 문자열로 인식되어 그대로 출력됨
    println("4 + 29 - 50") // 4 + 29 - 50이 출력됨 (연산되지 않음)
}