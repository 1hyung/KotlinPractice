fun main() {
    // main 함수는 프로그램 실행의 시작점(entry point)입니다.

    for (i in 1..5) {
        // 디버깅(Debugging)이란?
        // - 프로그램을 실행하면서 코드의 동작을 단계별로 분석하고, 예상치 못한 동작을 수정하는 과정입니다.

        // 브레이크포인트(Breakpoint)란?
        // - 디버깅 중 특정 코드 줄에서 실행을 잠시 멈출 수 있도록 지정하는 기능입니다.
        // - 브레이크포인트를 설정하면 코드가 해당 줄에서 멈추고 변수 값 등을 확인할 수 있습니다.

        // IntelliJ IDEA에서 디버깅 모드 실행 방법
        // 1. `Ctrl + F8`을 눌러 특정 코드 줄에 브레이크포인트(🔴)를 설정합니다.
        // 2. `Shift + F9`를 눌러 디버그(Debug) 모드를 시작합니다.
        // 3. 실행이 브레이크포인트에서 멈추면, 변수 `i` 값을 확인할 수 있습니다.
        // 4. `F8`을 눌러 한 줄씩 실행(Step Over)할 수 있으며, `F7`을 눌러 함수 내부로 진입(Step Into)할 수도 있습니다.

        println("i = $i") // `println`은 변수 `i` 값을 출력하는 역할을 합니다.
        // 브레이크포인트를 이 줄에 설정하면, `i`의 값이 변하는 과정을 디버깅할 수 있습니다.
    }
}